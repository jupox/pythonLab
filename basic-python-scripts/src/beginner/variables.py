# This file introduces variables in Python, showing how to declare and use different data types such as integers, floats, strings, and booleans.

# Integer variable
age = 25
print("Age:", age)

# Float variable
height = 5.9
print("Height:", height)

# String variable
name = "Alice"
print("Name:", name)

# Boolean variable
is_student = True
print("Is Student:", is_student)

# Demonstrating type conversion
age_as_string = str(age)
print("Age as string:", age_as_string)