# This file demonstrates the use of loops in Python, including for loops and while loops.

# For loop example: Iterating over a list
fruits = ["apple", "banana", "cherry"]
print("Fruits in the list:")
for fruit in fruits:
    print(fruit)

# While loop example: Counting down from 5
count = 5
print("\nCounting down:")
while count > 0:
    print(count)
    count -= 1
print("Blast off!")